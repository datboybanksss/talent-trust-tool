// Summary data for dashboard cross-reference
export const getLifeFileSummary = (
  beneficiaries: Beneficiary[],
  contacts: EmergencyContact[],
  documents: LifeFileDocument[],
  assets: LifeFileAsset[],
) => {
  const insurances = assets.filter(a => a.asset_category === "insurance");
  const investments = assets.filter(a => a.asset_category === "investment");
  const totalInsuranceCover = insurances.reduce((s, a) => s + (a.amount || 0), 0);
  const totalInvestmentValue = investments.reduce((s, a) => s + (a.amount || 0), 0);
  const completedDocs = documents.filter(d => d.status === "complete").length;
  const totalAllocation = beneficiaries.reduce((s, b) => s + (Number(b.allocation_percentage) || 0), 0);

  return {
    beneficiaryCount: beneficiaries.length,
    contactCount: contacts.length,
    documentCount: documents.length,
    completedDocs,
    insurancePolicies: insurances.length,
    totalInsuranceCover,
    investmentCount: investments.length,
    totalInvestmentValue,
    totalAllocation,
    allocationComplete: totalAllocation === 100,
    needsAttention: documents.filter(d => d.status !== "complete").length,
  };
};
